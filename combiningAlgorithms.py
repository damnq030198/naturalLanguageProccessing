from nltk.classify import ClassifierI
from statistics import mode
import nltk
import ml_algorithms

class VoteClassifier(ClassifierI):
    def __init__(self, *classifiers):
        self._classifiers = classifiers
    
    def classify(self, features):
        votes = []
        for c in self._classifiers:
            v = c.classify(features)
            votes.append(v)
        return mode(votes)
    
    def confidence(self, features):
        votes = []
        for c in self._classifiers:
            v = c.classify(features)
            votes.append(v)

        choice_votes = votes.count(mode(votes))
        conf = choice_votes / len(votes)
        return conf

classifier = ml_algorithms.classifier
MNB_classifier = ml_algorithms.MNB_classifier
BernoulliNB_classifier  = ml_algorithms.BernoulliNB_classifier
LogisticRegression_classifier = ml_algorithms.LogisticRegression_classifier
SGDClassifier_classifier = ml_algorithms.SGDClassifier_classifier
LinearSVC_classifier = ml_algorithms.LinearSVC_classifier
NuSVC_classifier  = ml_algorithms.NuSVC_classifier


voted_classifier = VoteClassifier(classifier,
                                  NuSVC_classifier,
                                  LinearSVC_classifier,
                                  SGDClassifier_classifier,
                                  MNB_classifier,
                                  BernoulliNB_classifier,
                                  LogisticRegression_classifier)
testing_set = ml_algorithms.testing_set
print("voted_classifier accuracy percent:", (nltk.classify.accuracy(voted_classifier, testing_set))*100)

print("Classification:", voted_classifier.classify(testing_set[0][0]), "Confidence %:",voted_classifier.confidence(testing_set[0][0])*100)
